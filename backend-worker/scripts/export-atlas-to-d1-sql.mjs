#!/usr/bin/env node
import { randomUUID } from "node:crypto";
import { writeFile } from "node:fs/promises";

const uri = process.env.MAM_MONGODB_URI;
const dbName = process.env.MAM_MONGODB_DB || "mam_pivko";
const outputPath = process.argv[2] || "d1-import.sql";

if (!uri) {
  console.error("MAM_MONGODB_URI is required.");
  process.exit(1);
}

let MongoClient;
({ MongoClient } = await import("mongodb"));
try {
  ({ MongoClient } = await import("mongodb"));
} catch {
  console.error("This migration helper needs the mongodb package. Run it with: npx -p mongodb node scripts/export-atlas-to-d1-sql.mjs");
  process.exit(1);
}

function sqlString(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlNumber(value) {
  return typeof value === "number" && Number.isFinite(value) ? String(value) : "NULL";
}

function isoDate(value, fallback = new Date()) {
  const date = value instanceof Date ? value : new Date(value || fallback);
  return Number.isNaN(date.getTime()) ? fallback.toISOString() : date.toISOString();
}

function normalizePub(pub) {
  return {
    name: pub?.name || "",
    address: pub?.address || "",
    notes: pub?.notes || "",
    url: pub?.url || "",
    mapy_lon: pub?.mapy_lon ?? null,
    mapy_lat: pub?.mapy_lat ?? null,
    mapy_label: pub?.mapy_label || ""
  };
}

const client = new MongoClient(uri);
await client.connect();

try {
  const database = client.db(dbName);
  const [events, wishlist] = await Promise.all([
    database.collection("events").find({}).sort({ date: -1 }).toArray(),
    database.collection("wishlist").find({}).sort({ created_at: -1 }).toArray()
  ]);

  const statements = [
    "-- Generated by scripts/export-atlas-to-d1-sql.mjs",
    "-- Review before importing. Existing Mongo _id values are intentionally replaced with new UUIDs."
  ];

  for (const event of events) {
    const pubsJson = JSON.stringify(Array.isArray(event.pubs) ? event.pubs.map(normalizePub) : []);
    statements.push(
      `INSERT INTO events (id, name, date, organizer, pubs_json, notes, created_at, updated_at) VALUES (${[
        sqlString(randomUUID()),
        sqlString(event.name || ""),
        sqlString(isoDate(event.date)),
        sqlString(event.organizer || ""),
        sqlString(pubsJson),
        sqlString(event.notes || ""),
        sqlString(isoDate(event.created_at, new Date(event.date || Date.now()))),
        sqlString(isoDate(event.updated_at, new Date(event.created_at || event.date || Date.now())))
      ].join(", ")});`
    );
  }

  for (const item of wishlist) {
    statements.push(
      `INSERT INTO wishlist (id, name, address, notes, url, mapy_lon, mapy_lat, mapy_label, created_at) VALUES (${[
        sqlString(randomUUID()),
        sqlString(item.name || ""),
        sqlString(item.address || ""),
        sqlString(item.notes || ""),
        sqlString(item.url || ""),
        sqlNumber(item.mapy_lon),
        sqlNumber(item.mapy_lat),
        sqlString(item.mapy_label || ""),
        sqlString(isoDate(item.created_at))
      ].join(", ")});`
    );
  }

  statements.push("");
  await writeFile(outputPath, statements.join("\n"), "utf8");
  console.log(`Wrote ${events.length} events and ${wishlist.length} wishlist items to ${outputPath}.`);
} finally {
  await client.close();
}
